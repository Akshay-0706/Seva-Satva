package com.official.sevasatva;

public class mentorAllocationModel {
    String name, code;

    public mentorAllocationModel() {
    }

    public mentorAllocationModel(String name, String code) {
        this.name = name;
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }
}
